const GW = 3
const GH = 5

export interface TextBitmap { w: number; h: number; on: Uint8Array }

// fontHeight>=10 → 放大 2 倍(纯方块);否则原生 5px
export function renderText(text: string, fh: number): TextBitmap {
  const s = fh >= 10 ? 2 : 1
  const gap = 1
  const chars = [...text.toUpperCase()].map((c) => FONT3x5[c] || FONT3x5[' '])
  const cols = chars.length ? chars.length * GW + (chars.length - 1) * gap : 0
  const w = cols * s
  const h = GH * s
  const on = new Uint8Array(Math.max(1, w * h))
  let cx = 0
  for (const g of chars) {
    for (let gy = 0; gy < GH; gy++) for (let gx = 0; gx < GW; gx++) {
      if (g[gy][gx] !== '#') continue
      for (let sy = 0; sy < s; sy++) for (let sx = 0; sx < s; sx++) {
        const X = (cx + gx) * s + sx
        const Y = gy * s + sy
        on[Y * w + X] = 1
      }
    }
    cx += GW + gap
  }
  return { w, h, on }
}
